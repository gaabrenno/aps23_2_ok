    private void bSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSelectActionPerformed
        // TODO add your handling code here:
        ListaPDAO dao = new ListaPDAO();
        ListaP P = new ListaP();
        P.setRegistro(Integer.parseInt(TxtPesquisa.getText()));
        long iniciar = System.currentTimeMillis();
        dao.select(P);
        //TxtNome.setText(P.getNome());
        TxtNome.setText(dao.select(P).getNome());
        BufferedImage imagem = null;
        try{
            imagem = ImageIO.read(new ByteArrayInputStream(dao.select(P).getImg()));
        }catch(Exception e) {
            
        }
        ImageIcon icon = new ImageIcon(imagem);
        Icon foto = new ImageIcon(icon.getImage().getScaledInstance(lFoto.getWidth(), lFoto.getHeight(), Image.SCALE_SMOOTH));
        lFoto.setIcon(foto);
        TxtRegistro.setText(TxtPesquisa.getText());
        long fi = System.currentTimeMillis();
        JOptionPane.showMessageDialog(null, "Valor encontrado /n Tempo de pesquisa "+(iniciar -fi)+"ms");
    }//GEN-LAST:event_bSelectActionPerformed

