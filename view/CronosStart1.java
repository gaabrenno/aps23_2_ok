    private void bBinariaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBinariaActionPerformed
        // TODO add your handling code here:
       int x = Integer.parseInt(TxtPesquisa.getText());
       int inicio = 0, fim = tResultado.getRowCount()-1;
       long iniciar = System.currentTimeMillis();
       int meio;
           
       while (inicio <= fim) {
           meio = (inicio + fim) / 2;
           
           if (x == Integer.parseInt(tResultado.getValueAt(meio, 0).toString())){
               long fi = System.currentTimeMillis();

               JOptionPane.showMessageDialog(null, "Valor encontrado na posição "+meio+"/n Tempo de pesquisa "+(iniciar - fi)+"ms");
           }
           if (x < Integer.parseInt(tResultado.getValueAt(meio, 0).toString())){
               fim = meio - 1;
               System.out.println(Integer.parseInt(tResultado.getValueAt(meio, 0).toString()));
           }else{
               inicio = meio + 1;
           }
       }
    }//GEN-LAST:event_bBinariaActionPerformed

