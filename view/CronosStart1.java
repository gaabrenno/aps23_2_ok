    private void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) tResultado.getModel();
        modelo.setNumRows(0);
        ListaPDAO cdao = new ListaPDAO();
        for (ListaP c: cdao.read()){
            modelo.addRow(new Object[]{
                c.getRegistro(),
                c.getNome(),
                c.getImg(),
            });
            
        }
    }
